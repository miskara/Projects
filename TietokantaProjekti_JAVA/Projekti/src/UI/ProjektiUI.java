    private void lisääActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lisääActionPerformed
       if(isDouble(Hinta1.getText())==false){
           Hinta1.setText("Syötä oikea arvo!");
       }
       else{
        int[] aineet = aineList1.getSelectedIndexes();
        int[] maarat = new int[6];
        JSlider[] j = new JSlider[6];
        j[0] = jSlider2;
        j[1] = jSlider3;
        j[2] = jSlider4;
        j[3] = jSlider5;
        
        for(int i = 0; i < aineet.length; i++){
        maarat[i] = j[i].getValue();
                }
        
        k.lisääDrinkki(drinkinNimi.getText(), Double.parseDouble(Hinta1.getText()), aineet,maarat);
        drinkinNimi.setText("");
        aineList1.clear();
        drinkkiLisää.hide();
        asetatiedot();
        jSlider2.setVisible(false);
        jSlider3.setVisible(false);
        jSlider4.setVisible(false);
        jSlider5.setVisible(false);
        label8.setVisible(false);
        label9.setVisible(false);
        label10.setVisible(false);
        label11.setVisible(false);
        //lisää.setVisible(false);
       }
    }//GEN-LAST:event_lisääActionPerformed

