    private void lisää_ainesosaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lisää_ainesosaActionPerformed
        
        if (ainesosan_nimi.getText()==null || isInteger(määrä.getText()) == false) {
            ainesosan_nimi.grabFocus();
            määrä.setText("");
        } else {
            k.lisääAinesosa(ainesosan_nimi.getText(), Integer.parseInt(määrä.getText()));
            ainesosan_nimi.setText("");
            määrä.setText("");
            ainesosaLisää.hide();
            asetatiedot();
        }
    
    }//GEN-LAST:event_lisää_ainesosaActionPerformed

